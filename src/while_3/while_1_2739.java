package while_3;

import java.util.Scanner;

public class while_1_2739 {
    public static void main(String[] args) {


        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int x = 0;
        while (x < 9) {
            x++;
            System.out.println(a + " * " + x+ " = "+ a * x);

        }
    }

}
